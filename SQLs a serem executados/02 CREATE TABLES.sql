---------SEQUENCE---------------------

--Sequence do código de usuário
CREATE SEQUENCE SEQ_CODIGO_USUARIO
AS INT
START WITH 0
INCREMENT BY 1
MAXVALUE 99999
MINVALUE 0
CYCLE
CACHE 10;



---------INDEXES---------------------

CREATE INDEX IND_USUARIO 
ON USUARIO (CODIGO)
INCLUDE (NOME, TELEFONE, EMAILINSTITUCIONAL);

CREATE INDEX IND_CARONA
ON CARONA (HORARIOPARTIDA, HORARIOCHEGADA)
INCLUDE (PONTOSINTERMEDIARIOS, PONTODESTINO);