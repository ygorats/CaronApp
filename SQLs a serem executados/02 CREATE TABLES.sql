--Tabela de Usuário
IF NOT (EXISTS(SELECT * FROM INFORMATION_SCHEMA.TABLES
			WHERE TABLE_NAME ='USUARIO'))
BEGIN
	CREATE TABLE USUARIO(
		ID UNIQUEIDENTIFIER ,
		CODIGO NCHAR(8),
		CPF NCHAR(11),
		NOME VARCHAR(50),
		DTNASCIMENTO DATE,
		EMAILINSTITUCIONAL VARCHAR(40),
		EMAILSECUNDARIO VARCHAR(40),
		TELEFONE NCHAR(14),
		POSSUIVEICULO BIT,
		ATIVO BIT,
		SENHA VARCHAR(15),
		CONSTRAINT UNIQ_ID UNIQUE (ID),
		CONSTRAINT PK_CODIGO PRIMARY KEY (CODIGO),
		CONSTRAINT UNIQ_CPF UNIQUE (CPF),
		CONSTRAINT UNIQ_EMAIL UNIQUE (EMAILINSTITUCIONAL)
	) ON USUARIOS;
END;


--Tabela de veículo
IF NOT (EXISTS(SELECT * FROM INFORMATION_SCHEMA.TABLES
			WHERE TABLE_NAME ='VEICULO'))
BEGIN
	CREATE TABLE VEICULO(
		ID UNIQUEIDENTIFIER ,
		IDUSUARIO UNIQUEIDENTIFIER,
		PLACA NCHAR(7) PRIMARY KEY,
		MARCA VARCHAR(20),
		MODELO VARCHAR(30)
	);
END;


--Tabela de Carona
IF NOT (EXISTS(SELECT * FROM INFORMATION_SCHEMA.TABLES
			WHERE TABLE_NAME ='CARONA'))
BEGIN
	CREATE TABLE CARONA(
		ID uniqueidentifier PRIMARY KEY,
		IDUSUARIO uniqueidentifier,
		DESCRICAO varchar(50),	
		PONTOPARTIDA geography,
		PONTOSINTERMEDIARIOS geography,
		PONTODESTINO geography,
		HORARIOPARTIDA time,
		HORARIOCHEGADA time
	); 
END;


